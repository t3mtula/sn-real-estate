/**
 * Replace template placeholders + legacy hardcoded landlord name.
 * Ported from v1 RentalManagement.html renderTemplateText().
 */
export function renderTemplateText(
  text: string,
  ctx: { landlord?: string; tenant?: string },
): string {
  if (!text) return ''
  const landlord = ctx.landlord ?? ''
  const tenant = ctx.tenant ?? ''
  let out = text
    .replace(/\{\{tenant\}\}/g, tenant)
    .replace(/\{\{landlord\}\}/g, landlord)
  // Legacy fallback: when intro has the literal "บริษัท สมบัตินภา จำกัด" and the
  // contract's landlord is someone else, replace it. Same hack as v1.
  if (landlord && !landlord.includes('บริษัท สมบัตินภา')) {
    out = out.replace(/บริษัท\s*สมบัตินภา\s*จำกัด/g, landlord)
  }
  return out
}

/**
 * Convert a chunk of clause HTML (only <strong>…</strong> tags supported)
 * into pdfmake inline content [{text}, {text, bold:true}, ...].
 * v1 templates use HTML for emphasis; pdfmake doesn't render HTML directly.
 */
export type InlinePart = { text: string; bold?: boolean }

export function htmlToInlineParts(html: string): InlinePart[] {
  if (!html) return [{ text: '' }]
  const out: InlinePart[] = []
  const re = /<strong>([\s\S]*?)<\/strong>/g
  let lastIndex = 0
  let m: RegExpExecArray | null
  // biome-ignore lint/suspicious/noAssignInExpressions: classic regex exec loop
  while ((m = re.exec(html)) !== null) {
    if (m.index > lastIndex) {
      out.push({ text: html.slice(lastIndex, m.index) })
    }
    out.push({ text: m[1] ?? '', bold: true })
    lastIndex = m.index + m[0].length
  }
  if (lastIndex < html.length) {
    out.push({ text: html.slice(lastIndex) })
  }
  return out.length === 0 ? [{ text: '' }] : out
}
